# src/strategies/enums.py

"""
Перечисления (Enums) для стандартизации и надежного представления различных
состояний и категорий в аналитических данных арбитражного бота.

Использование Enum'ов вместо строковых литералов обеспечивает:
- Защиту от опечаток на этапе разработки
- Автодополнение в IDE
- Централизованное управление состояниями
- Самодокументируемый код
- Защиту от случайных изменений значений

Принцип разделения ответственности:
- Этот файл содержит только логические состояния (auto())
- Визуальное представление вынесено в src/bot/logic/report_maps.py
"""

from enum import Enum, auto


class OpportunityType(Enum):
    """
    Типы арбитражных возможностей для базовой классификации.

    Используется для категоризации найденных торговых возможностей
    по их потенциальной прибыльности и валидности.
    """
    HIGH_PROFIT = auto()  # Высокоприбыльная возможность
    LOW_PROFIT = auto()  # Низкоприбыльная возможность
    PHANTOM_ROI = auto()  # Аномальный/подозрительный ROI
    PHANTOM_LIQUIDITY = auto()  # Недостаточная ликвидность
    PHANTOM_LIMITS = auto()  # Ниже минимальных лимитов биржи


class RiskLevel(Enum):
    """
    Уровень риска, ассоциированный с торговой возможностью.

    Определяется на основе анализа технических индикаторов,
    волатильности, объемов торгов и других факторов.
    """
    LOW = auto()  # Низкий риск - безопасная сделка
    MEDIUM = auto()  # Средний риск - требует осторожности
    HIGH = auto()  # Высокий риск - потенциально опасно
    EXTREME = auto()  # Экстремальный риск - крайне опасно
    UNDEFINED = auto()  # Не удается определить уровень риска


class MACDSignal(Enum):
    """
    Сигнал, генерируемый индикатором MACD (Moving Average Convergence Divergence).

    MACD показывает взаимосвязь между двумя скользящими средними цены актива
    и используется для определения направления тренда.
    """
    BULLISH = auto()  # Бычий сигнал - рост цены
    BEARISH = auto()  # Медвежий сигнал - падение цены
    NEUTRAL = auto()  # Нейтральный сигнал - неопределенность


class RSIStatus(Enum):
    """
    Состояние актива по индикатору RSI (Relative Strength Index).

    RSI измеряет скорость и изменение ценовых движений,
    помогая определить условия перекупленности/перепроданности.
    Значения: 0-30 (перепроданность), 30-70 (нормально), 70-100 (перекупленность)
    """
    OVERSOLD = auto()  # Перепроданность (RSI < 30)
    HEALTHY = auto()  # Здоровое состояние (RSI 30-70)
    OVERBOUGHT = auto()  # Перекупленность (RSI > 70)
    CRITICAL_OVERBOUGHT = auto()  # Критическая перекупленность (RSI > 90)


class BollingerBandStatus(Enum):
    """
    Положение цены относительно Полос Боллинджера.

    Полосы Боллинджера состоят из простого скользящего среднего (средняя линия)
    и двух стандартных отклонений выше и ниже него.
    Помогают определить волатильность и потенциальные точки разворота.
    """
    ABOVE_UPPER = auto()  # Цена выше верхней полосы
    BELOW_LOWER = auto()  # Цена ниже нижней полосы
    INSIDE_BANDS = auto()  # Цена внутри полос
    ON_MIDDLE_BAND = auto()  # Цена на средней линии


class MarketTrend(Enum):
    """
    Общий характер тренда цены, определяемый по sparkline-графику.

    Анализирует последовательность ценовых движений для классификации
    текущего состояния рынка и прогнозирования дальнейшего поведения цены.
    """
    STABLE_GROWTH = auto()  # Стабильный устойчивый рост
    ACCELERATING_GROWTH = auto()  # Ускоряющийся рост
    GROWTH_WITH_SLOWDOWN = auto()  # Рост с замедлением
    CONSOLIDATION = auto()  # Консолидация/боковое движение
    STABLE_DECLINE = auto()  # Стабильное снижение
    SHARP_FALL = auto()  # Резкое падение ("падающий нож")
    PUMP_AND_DUMP = auto()  # Памп и дамп (искусственная накачка)
    REBOUND = auto()  # Отскок после падения


class ExchangeStatus(Enum):
    """
    Статус работы биржи для мониторинга доступности торговых операций.
    """
    ACTIVE = auto()  # Биржа работает нормально
    MAINTENANCE = auto()  # Техническое обслуживание
    DEGRADED = auto()  # Частичные проблемы
    OFFLINE = auto()  # Биржа недоступна


class OrderType(Enum):
    """
    Типы торговых ордеров.
    """
    MARKET = auto()  # Рыночный ордер
    LIMIT = auto()  # Лимитный ордер
    STOP_LOSS = auto()  # Стоп-лосс
    TAKE_PROFIT = auto()  # Тейк-профит


class ArbitrageStatus(Enum):
    """
    Статусы выполнения арбитражных операций.
    """
    PENDING = auto()  # Ожидает выполнения
    IN_PROGRESS = auto()  # Выполняется
    COMPLETED = auto()  # Успешно завершена
    FAILED = auto()  # Не удалось выполнить
    CANCELLED = auto()  # Отменена пользователем
    TIMEOUT = auto()  # Истекло время ожидания

class DensityAction(Enum):
    """
    Рекомендации к действию на основе анализа плотностей.
    """
    CONSIDER_LONG = auto()      # Рассмотреть LONG (сильная поддержка)
    CONSIDER_SHORT = auto()     # Рассмотреть SHORT (сильное сопротивление)
    WAIT_AND_WATCH = auto()     # Ждать и наблюдать (баланс сил)
    NEUTRAL = auto()            # Нейтрально (нет данных для вывода)