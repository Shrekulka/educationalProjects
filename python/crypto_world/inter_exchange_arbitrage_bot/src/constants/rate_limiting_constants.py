"""
Конфигурация "умных" повторных попыток для API-запросов.

Описывает стратегию "Экспоненциальной задержки" (Exponential Backoff).
Это позволяет боту быть "вежливым" и эффективным при временных сбоях на сервере биржи.

Аналогия: Представьте, что вы стучите в дверь, а вам не открывают.
- Глупый подход: стучать каждую секунду без остановки (это создаст раздражение).
- Умный подход (этот конфиг):
  1. Постучать. Не открыли.
  2. Подождать немного (base_delay_seconds).
  3. Постучать снова. Опять не открыли.
  4. Подождать уже дольше (предыдущая задержка * backoff_multiplier).
  5. Повторять, пока не откроют или пока не надоест (max_retries).

Как это работает на примере текущих настроек:
- Бот делает 1-й запрос. Неудача.
- Ждет 1.0 секунду (base_delay_seconds).
- Бот делает 2-й запрос. Опять неудача.
- Ждет 1.0 * 2.0 = 2.0 секунды.
- Бот делает 3-й запрос. Снова неудача.
- Ждет 2.0 * 2.0 = 4.0 секунды.
- Бот делает 4-й (последний) запрос. Если и он неудачен, выбрасывается ошибка.
"""
RATE_LIMIT_RETRY_CONFIG = {
    # Сколько раз ПОВТОРИТЬ попытку после первого неудачного запроса.
    # Итого максимальное число запросов = 1 (первый) + 3 (повторных) = 4.
    'max_retries': 3,

    # С какой задержки начинать? Это время ожидания после ПЕРВОЙ неудачи.
    'base_delay_seconds': 1.0,

    # Во сколько раз увеличивать паузу после каждой следующей неудачи?
    # 2.0 означает "удваивать паузу каждый раз".
    'backoff_multiplier': 2.0,

    # Какой максимальной может быть пауза между запросами?
    # Если расчетная задержка (например, 128с) превысит это значение,
    # бот будет ждать не 128с, а всего 60с. Это защищает от слишком долгих пауз.
    'max_delay_seconds': 60.0
}