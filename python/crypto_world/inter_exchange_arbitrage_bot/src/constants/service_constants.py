# --- Настройки Диагностической Обертки (DiagnosticProviderWrapper) ---

# Список имен новостных провайдеров, которые исторически показывали нестабильность
# или склонность к "зависанию" при выполнении запросов.
# Провайдеры из этого списка будут автоматически обернуты в DiagnosticProviderWrapper,
# который принудительно прервет их работу по таймауту, предотвращая блокировку
# всего процесса сбора новостей.
#
# Аргументация: Это позволяет гибко управлять проблемными API без изменения
# кода NewsService. Если какой-то провайдер будет исправлен или новый начнет
# сбоить, достаточно будет отредактировать этот список.
NEWS_PROVIDER_DIAGNOSTIC_WRAPPER_TARGETS = {
    'CryptoCompare',
    'NewsAPI',
    'Messari',
    'CoinCap'
}

# Таймаут в секундах для провайдеров, обернутых в DiagnosticProviderWrapper.
# Если провайдер не ответит за это время, его задача будет принудительно
# завершена, и он вернет пустой результат, не мешая остальным.
# Значение 45 секунд выбрано как компромисс: достаточно для выполнения
# сложных запросов, но достаточно мало, чтобы не задерживать пользователя.
NEWS_PROVIDER_DIAGNOSTIC_TIMEOUT_SECONDS = 45


# =================================================================
# =============== ⚙️ КОНФИГУРАЦИЯ СЕРВИСА ПРОКСИ ⚙️ =================
# =================================================================

# Максимальное количество прокси для одновременного тестирования.
# Предотвращает перегрузку сети и CPU при запуске, особенно при использовании
# больших публичных списков прокси.
MAX_PROXIES_TO_TEST = 500