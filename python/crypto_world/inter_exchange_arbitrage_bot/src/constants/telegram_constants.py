# Функция для получения URL API, чтобы избежать циклического импорта
def get_telegram_api_url(token: str) -> str:
    """Возвращает URL Telegram API для указанного токена."""
    if not token:
        raise ValueError("Токен не может быть пустым.")
    return f"https://api.telegram.org/bot{token}"


